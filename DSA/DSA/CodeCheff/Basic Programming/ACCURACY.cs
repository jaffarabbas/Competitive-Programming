    internal class ACCURACY
    {
        public static void func()
        {
            int t = int.Parse(Console.ReadLine());
            while (t-- > 0)
            {
                int l = int.Parse(Console.ReadLine());
                int c = l % 3;
                Console.WriteLine(c == 0 ? 0 : (3 - c));
            }
        }
    }
